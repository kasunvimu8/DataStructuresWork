public class Cordinates {
    
    private int x;
    private int y;
    
    public Cordinates(int x,int y){
        this.x = x;
        this.y = y;
    }
    
    public int getX(){
        return this.x;
    }
    
    public int getY(){
        return this.y;
    }
}
